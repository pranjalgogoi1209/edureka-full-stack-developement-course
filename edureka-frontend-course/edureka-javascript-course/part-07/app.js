// for loop
for (let i = 0; i < 10; i++) {
  console.log(i);
}

console.log("**********");

// while loop
let i = 0;
while (i < 10) {
  console.log(i);
  i++;
}

console.log("**********");

// do while loop
let j = 0;
do {
  console.log(j);
  j++;
} while (j < 10);
